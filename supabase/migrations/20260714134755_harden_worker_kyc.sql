create or replace function public.has_founder_access()
returns boolean
language sql
stable
security definer
set search_path = ''
as $$
  select (select auth.uid()) is not null
    and (
      coalesce((auth.jwt() -> 'app_metadata' ->> 'is_founder')::boolean, false)
      or exists (
        select 1
        from public.founder_access fa
        where fa.user_id = (select auth.uid())
      )
    )
$$;

create or replace function public.is_founder()
returns boolean
language sql
stable
security definer
set search_path = ''
as $$
  select public.has_founder_access()
$$;

