-- Derniere barriere : meme un appel direct a la RPC de retrait est refuse
-- tant que le travailleur n'est pas verifie.
create or replace function public.require_verified_kyc_for_withdrawal()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  if new.kind = 'withdrawal' and exists (
    select 1
    from public.wallets w
    join public.profiles p on p.id = w.profile_id
    where w.id = new.wallet_id
      and p.role = 'worker'
      and p.kyc_status <> 'verified'
  ) then
    raise exception using
      errcode = '23514',
      message = 'Verification KYC requise avant tout retrait.';
  end if;
  return new;
end;
$$;

drop trigger if exists wallet_transactions_require_verified_kyc on public.wallet_transactions;
create trigger wallet_transactions_require_verified_kyc
  before insert on public.wallet_transactions
  for each row execute function public.require_verified_kyc_for_withdrawal();

