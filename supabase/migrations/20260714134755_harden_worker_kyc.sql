-- Les champs de contournement structure ne peuvent pas etre fabriques par
-- une ecriture directe depuis le navigateur.
create or replace function public.guard_structure_verification_fields()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  if (
    new.founder_bypass is true
    or new.verification_status = 'founder_bypass'
    or new.verification_method = 'founder'
  ) and not public.is_founder() then
    raise exception using
      errcode = '42501',
      message = 'Acces fondateur requis pour ce contournement.';
  end if;
  return new;
end;
$$;

drop trigger if exists structures_guard_verification on public.structures;
create trigger structures_guard_verification
  before insert or update of verification_status, verification_method, founder_bypass
  on public.structures
  for each row execute function public.guard_structure_verification_fields();

-- Historique immuable des changements KYC.
create table if not exists public.kyc_status_history (
  id bigint generated by default as identity primary key,
  profile_id uuid not null references public.profiles (id) on delete cascade,
  previous_status text,
  new_status text not null,
  reason text,
  source text not null default 'system',
  changed_by uuid references public.profiles (id) on delete set null,
  created_at timestamptz not null default now(),
  constraint kyc_status_history_previous_check
    check (previous_status is null or previous_status in ('not_started', 'requested', 'submitted', 'verified', 'rejected')),
  constraint kyc_status_history_new_check
    check (new_status in ('not_started', 'requested', 'submitted', 'verified', 'rejected')),
  constraint kyc_status_history_source_check
    check (source in ('system', 'mission_acceptance', 'worker_submit', 'founder_review'))
);

create index if not exists kyc_status_history_profile_created_idx
  on public.kyc_status_history (profile_id, created_at desc);

alter table public.kyc_status_history enable row level security;

drop policy if exists "kyc history: owner or founder read" on public.kyc_status_history;
create policy "kyc history: owner or founder read"
  on public.kyc_status_history for select
  to authenticated
  using (profile_id = (select auth.uid()) or (select public.is_founder()));

revoke all on table public.kyc_status_history from public, anon;
grant select on table public.kyc_status_history to authenticated;

-- Protege les colonnes d'autorisation et les donnees KYC contre les UPDATE
-- directs. Les transitions legitimes passent par les fonctions ci-dessous.
create or replace function public.guard_profile_protected_fields()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_uid uuid := auth.uid();
  v_source text := coalesce(current_setting('app.kyc_source', true), '');
  v_kyc_changed boolean;
begin
  if new.id is distinct from old.id
     or new.role is distinct from old.role
     or new.created_at is distinct from old.created_at then
    raise exception using
      errcode = '42501',
      message = 'Les champs système du profil ne sont pas modifiables.';
  end if;

  v_kyc_changed :=
    new.kyc_status is distinct from old.kyc_status
    or new.kyc_requested_at is distinct from old.kyc_requested_at
    or new.kyc_submitted_at is distinct from old.kyc_submitted_at
    or new.iban_country is distinct from old.iban_country
    or new.iban_last4 is distinct from old.iban_last4
    or new.identity_document_name is distinct from old.identity_document_name
    or new.identity_document_path is distinct from old.identity_document_path
    or new.identity_document_uploaded_at is distinct from old.identity_document_uploaded_at;

  if not v_kyc_changed then
    return new;
  end if;

  -- Migrations, service_role et triggers internes de ce fichier.
  if v_uid is null or v_source in ('mission_acceptance', 'worker_submit', 'founder_review') then
    return new;
  end if;

  if public.is_founder() then
    return new;
  end if;

  raise exception using
    errcode = '42501',
    message = 'Utilisez le parcours KYC sécurisé pour modifier ces informations.';
end;
$$;

drop trigger if exists profiles_guard_protected_fields on public.profiles;
create trigger profiles_guard_protected_fields
  before update on public.profiles
  for each row execute function public.guard_profile_protected_fields();

create or replace function public.log_kyc_status_change()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_source text := coalesce(nullif(current_setting('app.kyc_source', true), ''), 'system');
  v_reason text := nullif(current_setting('app.kyc_reason', true), '');
begin
  if new.kyc_status is distinct from old.kyc_status then
    insert into public.kyc_status_history (
      profile_id, previous_status, new_status, reason, source, changed_by
    ) values (
      new.id, old.kyc_status, new.kyc_status, v_reason, v_source, auth.uid()
    );
  end if;
  return new;
end;
$$;

drop trigger if exists profiles_log_kyc_status on public.profiles;
create trigger profiles_log_kyc_status
  after update of kyc_status on public.profiles
  for each row execute function public.log_kyc_status_change();

-- La demande KYC apparait uniquement apres l'acceptation d'une candidature.
create or replace function public.request_kyc_after_acceptance()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  if new.status = 'accepted' and old.status is distinct from 'accepted' then
    perform set_config('app.kyc_source', 'mission_acceptance', true);
    update public.profiles
    set kyc_status = 'requested',
        kyc_requested_at = coalesce(kyc_requested_at, now())
    where id = new.worker_id
      and role = 'worker'
      and kyc_status in ('not_started', 'rejected');
  end if;
  return new;
end;
$$;

drop trigger if exists applications_request_worker_kyc on public.applications;
create trigger applications_request_worker_kyc
  after update of status on public.applications
  for each row execute function public.request_kyc_after_acceptance();

