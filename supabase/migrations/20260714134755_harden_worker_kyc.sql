select set_config('app.kyc_source', 'mission_acceptance', true);
update public.profiles p
set kyc_status = 'requested',
    kyc_requested_at = coalesce(p.kyc_requested_at, now())
where p.role = 'worker'
  and p.kyc_status = 'not_started'
  and exists (
    select 1
    from public.applications a
    where a.worker_id = p.id
      and a.status in ('accepted', 'in_progress', 'payment_pending', 'completed')
  );
select set_config('app.kyc_source', '', true);

-- Soumission atomique : ne stocke jamais l'IBAN complet et exige que le
-- document existe dans le dossier du demandeur.
create or replace function public.submit_worker_kyc(
  p_iban_country text,
  p_iban_last4 text,
  p_document_name text,
  p_document_path text
)
returns public.profiles
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_uid uuid := auth.uid();
  v_profile public.profiles;
  v_country text := upper(btrim(coalesce(p_iban_country, '')));
  v_last4 text := upper(btrim(coalesce(p_iban_last4, '')));
  v_name text := btrim(coalesce(p_document_name, ''));
  v_path text := btrim(coalesce(p_document_path, ''));
begin
  if v_uid is null then
    raise exception using errcode = '42501', message = 'Authentification requise.';
  end if;
  if v_country !~ '^[A-Z]{2}$' or v_last4 !~ '^[A-Z0-9]{4}$' then
    raise exception using errcode = '22023', message = 'Informations IBAN invalides.';
  end if;
  if length(v_name) < 3 or length(v_name) > 160 then
    raise exception using errcode = '22023', message = 'Nom de document invalide.';
  end if;
  if v_path not like v_uid::text || '/%' then
    raise exception using errcode = '42501', message = 'Chemin de document invalide.';
  end if;
  if not exists (
    select 1 from storage.objects o
    where o.bucket_id = 'kyc-documents' and o.name = v_path
  ) then
    raise exception using errcode = '22023', message = 'Document KYC introuvable.';
  end if;

  perform set_config('app.kyc_source', 'worker_submit', true);
  update public.profiles
  set kyc_status = 'submitted',
      kyc_requested_at = coalesce(kyc_requested_at, now()),
      kyc_submitted_at = now(),
      iban_country = v_country,
      iban_last4 = v_last4,
      identity_document_name = v_name,
      identity_document_path = v_path,
      identity_document_uploaded_at = now()
  where id = v_uid
    and role = 'worker'
    and kyc_status in ('requested', 'rejected')
  returning * into v_profile;

  if not found then
    raise exception using errcode = '23514', message = 'Aucune demande KYC active pour ce compte.';
  end if;
  return v_profile;
end;
$$;

revoke execute on function public.submit_worker_kyc(text, text, text, text) from public, anon;
grant execute on function public.submit_worker_kyc(text, text, text, text) to authenticated;

-- Outils de revue manuelle, disponibles uniquement aux comptes explicitement
-- inscrits dans founder_access (ou app_metadata.is_founder=true).
create or replace function public.founder_list_kyc_submissions()
returns table (
  profile_id uuid,
  full_name text,
  kyc_status text,
  kyc_requested_at timestamptz,
  kyc_submitted_at timestamptz,
  iban_country text,
  iban_last4 text,
  identity_document_name text,
  identity_document_path text,
  identity_document_uploaded_at timestamptz
)
language plpgsql
stable
security definer
set search_path = ''
as $$
begin
  if not public.is_founder() then
    raise exception using errcode = '42501', message = 'Acces fondateur requis.';
  end if;
  return query
  select p.id, p.full_name, p.kyc_status, p.kyc_requested_at,
         p.kyc_submitted_at, p.iban_country, p.iban_last4,
         p.identity_document_name, p.identity_document_path,
         p.identity_document_uploaded_at
  from public.profiles p
  where p.role = 'worker' and p.kyc_status <> 'not_started'
  order by p.kyc_submitted_at desc nulls last, p.kyc_requested_at desc nulls last;
end;
$$;

create or replace function public.founder_set_kyc_status(
  p_profile_id uuid,
  p_status text,
  p_reason text default null
)
returns void
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_reason text := nullif(btrim(coalesce(p_reason, '')), '');
begin
  if not public.is_founder() then
    raise exception using errcode = '42501', message = 'Acces fondateur requis.';
  end if;
  if p_status not in ('verified', 'rejected') then
    raise exception using errcode = '22023', message = 'Decision KYC invalide.';
  end if;
  if p_status = 'rejected' and v_reason is null then
    raise exception using errcode = '22023', message = 'Un motif est requis pour refuser.';
  end if;

  perform set_config('app.kyc_source', 'founder_review', true);
  perform set_config('app.kyc_reason', coalesce(v_reason, ''), true);
  update public.profiles
  set kyc_status = p_status
  where id = p_profile_id and role = 'worker' and kyc_status = 'submitted';

  if not found then
    raise exception using errcode = '23514', message = 'Ce dossier KYC ne peut pas etre traite.';
  end if;
end;
$$;

